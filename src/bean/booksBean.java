package bean;

public class booksBean {
	private String ISBN;
	private String book_name;
	private String book_author;
	private String book_press;
	private int book_rest_num;
	private String book_type_no;
	private String book_details;
	private String picture;
	private String library_name;
	public String getISBN() {
		return ISBN;
	}
	public void setISBN(String iSBN) {
		ISBN = iSBN;
	}
	public String getBook_name() {
		return book_name;
	}
	public void setBook_name(String book_name) {
		this.book_name = book_name;
	}
	public String getBook_author() {
		return book_author;
	}
	public void setBook_author(String book_author) {
		this.book_author = book_author;
	}
	public String getBook_press() {
		return book_press;
	}
	public void setBook_press(String book_press) {
		this.book_press = book_press;
	}
	public int getBook_rest_num() {
		return book_rest_num;
	}
	public void setBook_rest_num(int book_rest_num) {
		this.book_rest_num = book_rest_num;
	}
	public String getBook_type_no() {
		return book_type_no;
	}
	public void setBook_type_no(String book_type_no) {
		this.book_type_no = book_type_no;
	}
	public String getBook_details() {
		return book_details;
	}
	public void setBook_details(String book_details) {
		this.book_details = book_details;
	}
	public String getPicture() {
		return picture;
	}
	public void setPicture(String picture) {
		this.picture = picture;
	}
	public String getLibrary_name() {
		return library_name;
	}
	public void setLibrary_name(String library_name) {
		this.library_name = library_name;
	}
	
	
	
}
